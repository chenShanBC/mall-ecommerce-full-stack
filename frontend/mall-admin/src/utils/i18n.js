export const t = (path, params = {}) => {
  const segments = path.split('.');
  let current = zhCN;
  for (const segment of segments) {
    current = current?.[segment];
  }
  const text = current ?? path;
  if (typeof text !== 'string') {
    return text;
  }
  return text.replace(/\{(\w+)\}/g, (_, key) => String(params[key] ?? `{${key}}`));
};
